//Logical Operators

console.log(true && false);
console.log(false || false);
console.log(true && true);
console.log((5<10) && (6<3));
console.log(!(6>3));
console.log(!(3>6));
console.log(!true);
console.log(!false);


//ShortCircuiting Examples
console.log(10 || 6);
console.log(10 && 6);
console.log(0 || -0);
console.log(0 && 10);